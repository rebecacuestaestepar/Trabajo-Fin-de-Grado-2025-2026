CREATE TABLE AULA(
    IDAULA VARCHAR(8) PRIMARY KEY,
    NOMBRE VARCHAR(20),
    EDIFICIO VARCHAR(6),
    PLANTA NUMERIC(1),
    CAPACIDAD NUMERIC(3) CHECK (CAPACIDAD >= 0),
    NUM_ORDENADORES NUMERIC(2) CHECK(NUM_ORDENADORES >= 0),
    ALTAVOCES BOOLEAN NOT NULL DEFAULT FALSE,
    PROYECTOR BOOLEAN NOT NULL DEFAULT FALSE,
    CAMARA BOOLEAN NOT NULL DEFAULT FALSE,
    ENCHUFES BOOLEAN,   --Mirar si la cantidad o que referencias tener
    M2 NUMERIC (4,2) CHECK(M2 >= 0),
    OTROS VARCHAR(30)
);

CREATE TABLE RESPONSABLE(
    DNI VARCHAR(9) PRIMARY KEY,
    NOMBRE VARCHAR(20) NOT NULL,
    APELLIDOS VARCHAR(30) NOT NULL,
    CORREO VARCHAR(30) UNIQUE NOT NULL,
    TELEFONO NUMERIC(9)
);

CREATE TABLE RESERVA(
    IDRESERVA VARCHAR(5) PRIMARY KEY,
    ID_AULA VARCHAR(8) NOT NULL REFERENCES AULA(ID_AULA) ON UPDATE CASCADE ON DELETE CASCADE,
    ID_DIA DATE NOT NULL REFERENCES DIA(ID_DIA) ON UPDATE CASCADE ON DELETE CASCADE,
    MOMENTO_RESERVA TIMESTAMP NOT NULL DEFAULT NOW(),
    ESTADO CHAR(1),
    TIPO CHAR(1),
    HORA_INICIO TIME NOT NULL,
    HORA_FIN TIME NOT NULL,
    CHECK (HORA_FIN > HORA_INICIO)
);

CREATE TABLE RESERVA_PUNTUAL (
    ID_RESERVA  VARCHAR(12) PRIMARY KEY REFERENCES RESERVA(ID_RESERVA) ON UPDATE CASCADE ON DELETE CASCADE,
    ID_RESPONSABLE VARCHAR(9) NOT NULL REFERENCES RESPONSABLE(ID_RESPONSABLE) ON UPDATE CASCADE ON DELETE CASCADE,
    ID_ASIGNATURA VARCHAR(10) REFERENCES ASIGNATURAS(ID_ASIGNATURA) ON UPDATE CASCADE ON DELETE CASCADE,
    MOTIVO VARCHAR(90),
    INICIO TIMESTAMPTZ NOT NULL,
    FIN TIMESTAMPTZ NOT NULL,
    CHECK (FIN > INICIO)
);

CREATE TABLE RESERVA_PERIODICA (
    ID_RESERVA       VARCHAR(12) PRIMARY KEY REFERENCES RESERVA(ID_RESERVA) ON UPDATE CASCADE ON DELETE CASCADE,
    ID_ASIGNATURA VARCHAR(10) NOT NULL REFERENCES ASIGNATURA(ID_ASIGNATURA) ON UPDATE CASCADE ON DELETE CASCADE,
    DIA_SEMANA       SMALLINT NOT NULL CHECK (dia_semana BETWEEN 1 AND 7), -- 1=Lunes..7=Domingo (ISO)
    FECHA_INICIO     DATE NOT NULL, -- PUEDE SER QUE NO HAGA FALTA YA QUE SE PUEDE SACAR DE LA FECHA DEL SEMESTRE
    FECHA_FIN        DATE NOT NULL,
    INTERVALO_SEMANAS SMALLINT NOT NULL DEFAULT 1 CHECK (INTERVALO_SEMANAS >= 1),
    CHECK (FECHA_FIN >= fecha_inicio)
);


CREATE TABLE DOCENTE(
    CÓDIGO VARCHAR(9) PRIMARY KEY,
    NOMBRE VARCHAR(30) NOT NULL,
    APELLIDOS VARCHAR(100) NOT NULL,
    CORREO VARCHAR(30) NOT NULL,
    TELEFONO VARCHAR(9),
);

CREATE TABLE ASIGNATURAS(
    IDASIGNATURA VARCHAR(10) PRIMARY KEY,
    NOMBRE VARCHAR(30) NOT NULL,
    ABREVIATURA VARCHAR(20),
    CURSO_GRADO NUMERIC(1) NOT NULL CHECK(CURSO_GRADO BETWEEN 1 AND 6),
    SEMESTRE_ACADEMICO NUMERIC(1) NOT NULL CHECK(SEMESTRE IN(1,12)),  -- Se denominan los semestres también en función del año en el que se enuentran
    ECTS NUMERIC(2) NOT NULL CHECK(ECTS >= 0),
    GRADO_ID VARCHAR(10) NOT NULL REFERENCES GRADO(GRADO_ID) ON UPDATE CASCADE ON DELETE CASCADE,
    TIPO CHAR(1),   --Para saber si es una obligatoria o es optativa
    HORAS_PRACTICAS NUMERIC(1)
);

CREATE TABLE ASIGNTURA_PROFESOR(
    DNI_PROFESOR VARCHAR(9) REFERENCES PROFESOR(DNI_PROFESOR) ON UPDATE CASCADE ON DELETE CASCADE,
    ID_ASIGNATURA VARCHAR(10) REFERENCES ASIGNATURAS(ID_ASIGNATURA) ON UPDATE CASCADE ON DELETE CASCADE,
    PRIMARY KEY(DNI_PROFESOR, ID_ASIGNATURA)
);

CREATE TABLE GRADO(
    GRADO_ID VARCHAR(10) PRIMARY KEY,
    NOMBRE VARCHAR(80) NOT NULL,
    ABREVIATURA VARCHAR(20) NOT NULL,
    COORDINADOR VARCHAR(30)
);



CREATE TABLE AULA(
    ID INT PRIMARY KEY,
    NOMBRE VARCHAR(100) UNIQUE NOT NULL,
    /*NOMBRE_EXCEL VARCHAR(20) UNIQUE NOT NULL,*/
    EDIFICIO VARCHAR(6),
    PLANTA NUMERIC(1),
    CAPACIDAD NUMERIC(3) CHECK (CAPACIDAD >= 0),
    NUM_ORDENADORES NUMERIC(2) CHECK(NUM_ORDENADORES >= 0),
    ALTAVOCES BOOLEAN DEFAULT FALSE,
    PROYECTOR BOOLEAN DEFAULT FALSE,
    CAMARA BOOLEAN  DEFAULT FALSE,
    ENCHUFES BOOLEAN DEFAULT FALSE,
    M2 NUMERIC (6,2) CHECK(M2 >= 0),
    OTROS VARCHAR(30),
    GRADO_ID VARCHAR(10) REFERENCES GRADO(IDGRADO) ON UPDATE CASCADE ON DELETE CASCADE, /* Los aulas suelen tener un grado como prioritario dependiendo del semestre que también cambian */
    SEMESTRE_ACADEMICO NUMERIC(2) CHECK(SEMESTRE_ACADEMICO BETWEEN 1 AND 12),
    CAMPUS CHAR(1)
);

CREATE TABLE RESPONSABLE(
    CORREO VARCHAR(50) PRIMARY KEY,
    NOMBRE VARCHAR(50) NOT NULL,
    APELLIDOS VARCHAR(100) NOT NULL,
    TELEFONO NUMERIC(9),
    CODIGO_DOCENTE VARCHAR(9) UNIQUE REFERENCES DOCENTE(CODIGO) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE RESERVA(
    IDRESERVA VARCHAR(12) PRIMARY KEY,
    -- IDRESERVA INT AUTO_INCREMENT PRIMARY KEY,
    ID_AULA INT NOT NULL REFERENCES AULA(ID) ON UPDATE CASCADE ON DELETE CASCADE,
    ID_DIA DATE NOT NULL REFERENCES DIA(DIA) ON UPDATE CASCADE ON DELETE CASCADE,
    ESTADO CHAR(1),
    TIPO CHAR(1),
    HORA_INICIO TIME NOT NULL,
    HORA_FIN TIME NOT NULL,
    CHECK (HORA_FIN > HORA_INICIO)
);

CREATE TABLE RESERVA_PUNTUAL (
    ID_RESERVA VARCHAR(12) PRIMARY KEY REFERENCES RESERVA(IDRESERVA) ON UPDATE CASCADE ON DELETE CASCADE,
    ID_RESPONSABLE VARCHAR(30) NOT NULL REFERENCES RESPONSABLE(CORREO) ON UPDATE CASCADE ON DELETE CASCADE,
    CAPACIDAD_SOLICITADA NUMERIC(3) CHECK (CAPACIDAD_SOLICITADA >= 0),
    MOTIVO VARCHAR(90),
    INICIO TIMESTAMP NOT NULL DEFAULT (CURRENT_DATE),
    FIN TIMESTAMP NOT NULL DEFAULT (CURRENT_DATE),
    NUM_ORDENADORES_SOLICITADOS NUMERIC(2) CHECK (NUM_ORDENADORES_SOLICITADOS >= 0),
    ALTAVOCES_SOLICITADOS BOOLEAN NOT NULL DEFAULT FALSE,
    PROYECTOR_SOLICITADO BOOLEAN NOT NULL DEFAULT FALSE,
    CAMARA_SOLICITADA BOOLEAN NOT NULL DEFAULT FALSE,
    ENCHUFES_SOLICITADOS BOOLEAN NOT NULL DEFAULT FALSE,
    MOMENTO_RESERVA TIMESTAMP NOT NULL DEFAULT NOW(),
    OBSERVACIONES VARCHAR(300),
    CHECK (FIN >= INICIO)
);

CREATE TABLE RESERVA_PERIODICA (
    ID_RESERVA VARCHAR(12) PRIMARY KEY REFERENCES RESERVA(IDRESERVA) ON UPDATE CASCADE ON DELETE CASCADE,
    -- ID_ASIGNATURA VARCHAR(10) NOT NULL REFERENCES ASIGNATURAS(IDASIGNATURA) ON UPDATE CASCADE ON DELETE CASCADE,
    ID_GRUPO VARCHAR(10) NOT NULL REFERENCES GRUPO(GRUPOID) ON UPDATE CASCADE ON DELETE CASCADE,
    DIA_SEMANA       SMALLINT NOT NULL CHECK (DIA_SEMANA BETWEEN 1 AND 7),
    FECHA_INICIO     DATE NOT NULL DEFAULT (CURRENT_DATE), /* PUEDE SER QUE NO HAGA FALTA YA QUE SE PUEDE SACAR DE LA FECHA DEL SEMESTRE */
    FECHA_FIN        DATE NOT NULL DEFAULT (CURRENT_DATE),
    INTERVALO_SEMANAS SMALLINT NOT NULL DEFAULT 1 CHECK (INTERVALO_SEMANAS >= 1),
    CHECK (FECHA_FIN >= FECHA_INICIO)
);


CREATE TABLE DOCENTE(
    CODIGO VARCHAR(9) PRIMARY KEY,
    NOMBRE VARCHAR(50) NOT NULL,
    APELLIDOS VARCHAR(100) NOT NULL,
    CORREO VARCHAR(50) NOT NULL,
    TELEFONO VARCHAR(9)
);

CREATE TABLE ASIGNATURAS(
    IDASIGNATURA VARCHAR(10) PRIMARY KEY,
    NOMBRE VARCHAR(100) NOT NULL,
    ABREVIATURA VARCHAR(20),
    CURSO_GRADO NUMERIC(1) NOT NULL CHECK(CURSO_GRADO BETWEEN 1 AND 6),
    SEMESTRE_ACADEMICO NUMERIC(2) NOT NULL CHECK(SEMESTRE_ACADEMICO BETWEEN 1 AND 12),  /* Se denominan los semestres también en función del año en el que se enuentran */
    ECTS NUMERIC(2) NOT NULL CHECK(ECTS >= 0),
    GRADO_ID VARCHAR(10) NOT NULL REFERENCES GRADO(IDGRADO) ON UPDATE CASCADE ON DELETE CASCADE,
    TIPO CHAR(1),   /* Para saber si es una obligatoria o es optativa */
    HORAS_PRACTICAS NUMERIC(1)
);

CREATE TABLE IMPARTE(
    CODIGO_DOCENTE VARCHAR(9) NOT NULL REFERENCES DOCENTE(CODIGO) ON UPDATE CASCADE ON DELETE CASCADE,
    ID_ASIGNATURA VARCHAR(10) NOT NULL REFERENCES ASIGNATURAS(IDASIGNATURA) ON UPDATE CASCADE ON DELETE CASCADE,
    PRIMARY KEY(CODIGO_DOCENTE, ID_ASIGNATURA)
);

CREATE TABLE GRADO(
    IDGRADO VARCHAR(10) PRIMARY KEY,
    NOMBRE VARCHAR(150) NOT NULL,
    ABREVIATURA VARCHAR(20) NOT NULL,
    COORDINADOR VARCHAR(30)
);


CREATE TABLE GRUPO(
    GRUPOID INT AUTO_INCREMENT PRIMARY KEY,
    NOMBRE CHAR(3),
    ID_ASIGNATURA VARCHAR(10) NOT NULL REFERENCES ASIGNATURAS(IDASIGNATURA) ON UPDATE CASCADE ON DELETE CASCADE,
    ID_AULA INT REFERENCES AULA(ID) ON UPDATE CASCADE ON DELETE CASCADE
);



CREATE TABLE CURSO(
    IDCURSO VARCHAR(10) PRIMARY KEY,    /* 2025-2026 */
    FECHA_INICIO DATE NOT NULL DEFAULT (CURRENT_DATE),
    FECHA_FIN DATE NOT NULL DEFAULT (CURRENT_DATE)
);

CREATE TABLE SEMESTRE(
    IDSEMESTRE VARCHAR(15) PRIMARY KEY,
    CURSO_ID VARCHAR(10) NOT NULL REFERENCES CURSO(IDCURSO) ON UPDATE CASCADE ON DELETE CASCADE,
    NUMERO NUMERIC(1) CHECK(NUMERO IN(1,2)),
    FECHA_INICIO DATE NOT NULL DEFAULT (CURRENT_DATE),
    FECHA_FIN DATE NOT NULL DEFAULT (CURRENT_DATE)  
);

CREATE TABLE DIA(
    DIA DATE PRIMARY KEY,
    ID_SEMESTRE VARCHAR(15) NOT NULL REFERENCES SEMESTRE(IDSEMESTRE) ON UPDATE CASCADE ON DELETE CASCADE,
    DIA_SEMANA NUMERIC CHECK(DIA_SEMANA BETWEEN 1 AND 7)

);

CREATE TABLE LECTIVO(
    ID_DIA DATE PRIMARY KEY,
    FOREIGN KEY(ID_DIA) REFERENCES DIA(DIA) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE CAMBIO_DOCENCIA(
    ID_DIA DATE PRIMARY KEY,
    SUSTITUYE_DIA NUMERIC CHECK(SUSTITUYE_DIA BETWEEN 1 AND 5),   /* VER SI ASÍ BIEN TRATADO O BUSCAR OTRA FORMA */
    FOREIGN KEY(ID_DIA) REFERENCES DIA(DIA) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE EXAMEN(
    ID_DIA DATE PRIMARY KEY,
    CONVOCATORIA NUMERIC(1) CHECK(CONVOCATORIA IN(1,2)),
    FOREIGN KEY(ID_DIA) REFERENCES DIA(DIA) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE FESTIVO(
    ID_DIA DATE PRIMARY KEY,
    NOMBRE VARCHAR(60),
    ALCANCE VARCHAR(20),
    FOREIGN KEY(ID_DIA) REFERENCES DIA(DIA) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE TFG(
    ID_DIA DATE PRIMARY KEY,
    CONVOCATORIA NUMERIC(1) CHECK(CONVOCATORIA IN(1,2)),
    FOREIGN KEY(ID_DIA) REFERENCES DIA(DIA) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE USUARIOS(
    ID INT AUTO_INCREMENT PRIMARY KEY,
    PASSWORD VARCHAR(150) NOT NULL,
    LAST_LOGIN TIMESTAMP NULL,
    IS_SUPERUSER BOOLEAN NOT NULL DEFAULT FALSE,
    USERNAME VARCHAR(100) NOT NULL UNIQUE,
    FIRST_NAME VARCHAR(100) NOT NULL,
    LAST_NAME VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(100) NOT NULL UNIQUE,
    IS_STAFF BOOLEAN NOT NULL DEFAULT FALSE,
    IS_ACTIVE BOOLEAN NOT NULL DEFAULT TRUE,
    DATE_JOINED TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

